//startsWith() : kendisine parametre olarak verilen değer doğrultusunda içeriğin başlangıç değerini kontrol ederek boolean türünde sonuç döndürür.

var metin = "Lorem ipsum dolor";
document.write(metin.startsWith("L") + "<br>");

//endsWith() : kendisine parametre olarak verilen değer doğrultusunda içeriğin bitiş değerini kontrol ederek boolean türünde sonuç döndürür.

var metin = "Lorem ipsum dolor";
document.write(metin.endsWith("r"));