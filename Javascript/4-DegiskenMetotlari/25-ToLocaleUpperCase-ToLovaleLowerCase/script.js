//toLocaleUpperCase() : değişken içerisindeki metnin tamamını tarayıcı diline göre büyük harfe dönüştürerek değeri geriye döndürür.

var metin = "şiöğü";
document.write(metin.toLocaleUpperCase() + "<br>");


//toLocaleUpperCase() : değişken içerisindeki metnin tamamını tarayıcı diline göre küçük harfe dönüştürerek değeri geriye döndürür.

var metin = "ŞİÖĞÜ";
document.write(metin.toLocaleLowerCase() + "<br>");
