//indexOf: dizi içerisinde parametrik olarak verilen değeri arar ve ilk eşleşmeyi sağlayan elemanın indis değerini geriye döndürür.

var isimler = ["Ulaş", "Gülhan", "Yiğit"];
document.write(isimler + "<br>");

var islemYap = isimler.indexOf("Yiğit");
document.write(islemYap + "<br>");


//lastIndexOf: dizi içerisinde parametrik olarak verilen değeri arar ve son eşleşmeyi sağlayan elemanın indis değerini geriye döndürür.

var isimler2 = ["Ulaş", "Gülhan", "Yiğit"];
document.write(isimler2 + "<br>");

var islemYap2 = isimler2.lastIndexOf("Gülhan");
document.write(islemYap2 + "<br>");