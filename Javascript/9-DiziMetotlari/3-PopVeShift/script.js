//pop: dizi içerisindeki en son elemanı siler ve geriye döndürür.
var isim = ["Ulaş", "Gülhan", "Yiğit", "isim"];
document.write(isim + "<br>");
var sonuc = isim.pop();
document.write(isim + "<br>");
document.write(sonuc + "<br>");

//shift: dizi içerisindeki ilk elemanı siler geriye döndürür

var isim2 = ["Ulaş", "Gülhan", "Yiğit", "isim"];
document.write(isim2 + "<br>");
var sonuc2 = isim2.shift();
document.write(isim2 + "<br>");
document.write(sonuc2 + "<br>");